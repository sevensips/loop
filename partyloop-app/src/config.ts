// В Expo Go на реальном телефоне "localhost" — это сам телефон, а не твой
// компьютер, поэтому для теста с устройства сюда нужно подставить:
//  - IP компьютера в локальной сети (например "http://192.168.1.42:3000"),
//    если телефон и комп в одном Wi-Fi;
//  - или https-адрес от ngrok/tunnel (например "https://abcd1234.ngrok.app"),
//    если хочешь тестировать откуда угодно.
// Для симулятора/эмулятора на том же компе localhost обычно работает как есть.
export const API_URL = 'http://192.168.1.106:3000';

// http(s) -> ws(s), чтобы не дублировать адрес отдельно для WebSocket
export const WS_URL = API_URL.replace(/^http/, 'ws') + '/ws';
